    private UserEntity createUser(final String username) {
        final var user = new UserEntity();
        user.username = username;
        user.password = "secret";
        user.rank = this.userRankRepository.findById(1L);
        this.userRepository.persist(user);
        return user;
    }

    private ExerciseEntity createExercise(final UserEntity user, final String title) {
        final var exercise = new ExerciseEntity();
        exercise.title = title;
        exercise.content = "test content";
        exercise.user = user;
        this.exerciseRepository.persist(exercise);
        return exercise;
    }
