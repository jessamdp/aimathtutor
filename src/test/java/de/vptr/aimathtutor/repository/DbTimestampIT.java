    // ------------------------------------------------------------------
    // CommentFlagEntity
    // ------------------------------------------------------------------

    @Test
    @TestTransaction
    void commentFlagTimestampsAreDbGenerated() {
        final var user = this.createUser("flag_user");
        final var exercise = this.createExercise(user, "Flag Exercise");
        final var comment = new CommentEntity();
        comment.content = "Flag me";
        comment.exercise = exercise;
        comment.user = user;
        this.commentRepository.persist(comment);
        this.em.flush();

        final var flag = new CommentFlagEntity();
        flag.comment = comment;
        flag.flagger = user;

        this.commentFlagRepository.persist(flag);
        this.em.flush();

        assertNotNull(flag.created, "CommentFlagEntity.created must be generated by the DB");
        assertNull(flag.lastEdit, "CommentFlagEntity.lastEdit must be null before update");

        this.em.createNativeQuery("UPDATE comment_flags SET id = id WHERE id = :id")
                .setParameter("id", flag.id)
                .executeUpdate();
        this.em.flush();
        this.em.refresh(flag);

        assertNotNull(flag.lastEdit, "CommentFlagEntity.lastEdit must be generated by the DB on update");
    }

