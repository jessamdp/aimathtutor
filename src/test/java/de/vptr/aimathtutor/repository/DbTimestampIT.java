    AiInteractionRepository aiInteractionRepository;

    @Inject
    AiConfigRepository aiConfigRepository;

    // ------------------------------------------------------------------
    // AiInteractionEntity
    // ------------------------------------------------------------------

    @Test
    @TestTransaction
    void aiInteractionTimestampsAreDbGenerated() {
        final var interaction = new AiInteractionEntity();
        interaction.eventType = "TEST";
        interaction.feedbackType = "HINT";

        this.aiInteractionRepository.persist(interaction);
        this.em.flush();

        assertNotNull(interaction.created, "AiInteractionEntity.created must be generated by the DB");
        assertNull(interaction.lastEdit, "AiInteractionEntity.lastEdit must be null before update");

        interaction.feedbackMessage = "updated";
        this.em.flush();

        assertNotNull(interaction.lastEdit, "AiInteractionEntity.lastEdit must be generated by the DB on update");
    }

    // ------------------------------------------------------------------
    // ExerciseEntity
    // ------------------------------------------------------------------

    @Test
    @TestTransaction
    void exerciseTimestampsAreDbGenerated() {
        final var user = this.createUser("exercise_user");
        final var exercise = new ExerciseEntity();
        exercise.title = "Test";
        exercise.content = "Content";
        exercise.user = user;

        this.exerciseRepository.persist(exercise);
        this.em.flush();

        assertNotNull(exercise.created, "ExerciseEntity.created must be generated by the DB");
        assertNull(exercise.lastEdit, "ExerciseEntity.lastEdit must be null before update");

        exercise.title = "Updated";
        this.em.flush();

        assertNotNull(exercise.lastEdit, "ExerciseEntity.lastEdit must be generated by the DB on update");
    }

